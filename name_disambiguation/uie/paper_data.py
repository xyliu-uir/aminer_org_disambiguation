def sort_str(strName):
    """
    字符串排序
    :param strName:
    :return:
    """
    list_a = strName.split(' ')
    list_a.sort()
    str_a = ' '.join(list_a)
    return str_a


def replace_jie_ci(str_a):
    """
    替换机构无用介词
    :param str_a:
    :return:
    """
    str_a = str.lower(str_a)
    str_list = str_a.split(' ')
    if "of" in str_list:
        str_list.remove("of")
    if "the" in str_list:
        str_list.remove("the")
    if "in" in str_list:
        str_list.remove("in")
    if "at" in str_list:
        str_list.remove("at")
    if "on" in str_list:
        str_list.remove("on")
    if "and" in str_list:
        str_list.remove("and")
    str_b = ' '.join(str_list)
    return str_b


